def PieceMap(piece):
    # Function to retrieve the piece map based on the piece type and colour
    piece_map = []
    if piece.code == "p":
        piece_map = map_points[0]
    elif piece.code == "b":
        piece_map = map_points[1]
    elif piece.code == "n":
        piece_map = map_points[2]
    elif piece.code == 'r':
        piece_map = map_points[3]
    elif piece.code == "q":
        piece_map = map_points[4]
    elif piece.code == "k":
        piece_map = map_points[5]

    # returns numpy.ndarray --> The piece map corresponding to the given piece

    # check colour of piece
    if piece.colour == 0:
        return piece_map # Return the piece map for white pieces
    else:
        return np.flip(piece_map) * (-1)  # Return the flipped and negated piece map for black pieces
